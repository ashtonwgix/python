# loop through a list 

languages = ["english" , "kiswahili" , "french" , "german" , "russia" , "chinese"]
for language in languages :
    print(language)

countries = ("Kenya" , "Tanzania" , "Ethiopia" , "Uganda" , "Somalia" , "Rwanda")
for country in countries :
    print(country)

Artists = ("Breezy" , "Caesar" , "Giveon" , "Jcole" , "Asap" , "Bigxtheplug")
for artist in Artists :
    print(artist)

Movies = ("deep water" , "in secret" , "doom" , "step up" , "f1" , "Grand turismo")
for movie in Movies :
    print(movie)