# student marks scenario
marks= 2

if (marks >=0 and marks <=30) :
    print("BELOW AVERAGE")

elif (marks >=30 and marks <=40) :
    print("AVERAGE")

elif (marks >=40 and marks <=70) :
    print("ABOBE AVERAGE")

elif (marks >=70 and marks <=100) :
    print("EXCELLENT")

else :
    print("INVALID MARKS")